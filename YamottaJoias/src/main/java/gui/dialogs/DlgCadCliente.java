    private void btnEditarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarClienteActionPerformed
        cpf_Escolhido = JOptionPane.showInputDialog("Informe o CPF do cliente:");
        Cliente cliente_editando = this.gerente.find_cliente(cpf_Escolhido);
        if (cliente_editando != null) {
            this.editando = true;
            this.LimparCampos();
            this.HabilitarCampos(true);
            objetoParaCampos(cliente_editando);
        } else {
            JOptionPane.showMessageDialog(this, "Cliente n√£o localizado/cadastrado!");
        }
    }//GEN-LAST:event_btnEditarClienteActionPerformed

    private void btnDeletarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarClienteActionPerformed
        String cpf_cliente = JOptionPane.showInputDialog("Informe o CPF do cliente que deseja excluir:");
        Cliente cliente_excluir = this.gerente.find_cliente(cpf_cliente);
        if (cliente_excluir != null) {
            gerente.rem_cliente(cpf_cliente);
            JOptionPane.showMessageDialog(this, "Excluido com sucesso!");
        } else
            JOptionPane.showMessageDialog(this, "N√£o localizado ou n√£o existente no banco de dados");
        this.LimparCampos();
        this.HabilitarCampos(false);
        String listagem = gerente.toString();
        edtListagemClientes.setText(listagem);
        gerente.salvarNoArquivo("ListagemClientes.json");
    }//GEN-LAST:event_btnDeletarClienteActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(this.editando == true){
            Cliente att_cliente = this.camposParaObjetoEditar();
            this.gerente.upt_cliente(cpf_Escolhido, att_cliente);
        }else{
            Cliente novo_cliente = this.camposParaObjeto();
            gerente.add_cliente(novo_cliente);
        }
        this.LimparCampos();
        this.HabilitarCampos(false);
        String listagem = gerente.toString();
        edtListagemClientes.setText(listagem);
        gerente.salvarNoArquivo("ListagemClientes.json");
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    public Cliente camposParaObjetoEditar() { //Utilizando segundo construtor para editar sem gerar um novo ID.
        String nome_cliente = edtNome.getText();
        String telefone_cliente = edtTelefone.getText();
        String cpf_cliente = edtCPF.getText();
        int id_cliente = Integer.parseInt(edtID.getText());
        Cliente cliente = new Cliente(nome_cliente, cpf_cliente, telefone_cliente, id_cliente);
        return cliente;
    }

    public Cliente camposParaObjeto() {
        Cliente cliente = new Cliente();
        cliente.setNome_cliente(edtNome.getText());
        cliente.setCpf_cliente(edtCPF.getText());
        cliente.setTelefone_cliente(edtTelefone.getText());
        return cliente;
    }

    public void objetoParaCampos(Cliente cliente) {
        edtNome.setText(cliente.getNome_cliente());
        edtCPF.setText(cliente.getCpf_cliente());
        edtTelefone.setText(cliente.getTelefone_cliente());
        edtID.setText(cliente.getId_cliente() + "");
    }

    public void HabilitarCampos(boolean flag) {
        edtCPF.setEnabled(flag);
        edtID.setEnabled(false);
        edtNome.setEnabled(flag);
        edtTelefone.setEnabled(flag);
    }

    public void LimparCampos() {
        edtCPF.setText("");
        edtID.setText("");
        edtNome.setText("");
        edtTelefone.setText("");
    }

