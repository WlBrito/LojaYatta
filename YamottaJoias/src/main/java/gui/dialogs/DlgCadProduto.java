    private void btnEditarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarProdutoActionPerformed
        String cod_Produto = JOptionPane.showInputDialog("Informe o codigo do produto:");
        this.cod_Escolhido = Integer.parseInt(cod_Produto);
        Produto produtoEditando = this.gerente.buscarProduto(cod_Escolhido);
        if (produtoEditando != null) {
            this.editando = true;
            this.limpar_Campos();
            this.habilitar_Campos(true);
            objetoParaCampos(produtoEditando);
        } else {
            JOptionPane.showMessageDialog(this, "Produto n찾o localizado/cadastrado!");
        }
    }//GEN-LAST:event_btnEditarProdutoActionPerformed

    private void btnDeletarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarProdutoActionPerformed
        String cod_produto = JOptionPane.showInputDialog("Informe o c처digo do prodduto que deseja EXCLUIR:");
        this.cod_Escolhido = Integer.parseInt(cod_produto);
        Produto produto_excluir = this.gerente.buscarProduto(cod_Escolhido);
        if (produto_excluir != null) {
            gerente.remProduto(produto_excluir.getIdProduto());
            JOptionPane.showMessageDialog(this, "Excluido com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "N찾o localizado");
        }
        limpar_Campos();
        habilitar_Campos(false);
        String listagem = gerente.toString();
        edtListagemProdutos.setText(listagem);
        gerente.salvarNoArquivo("ListagemProdutos.json");
    }//GEN-LAST:event_btnDeletarProdutoActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (this.editando == true) {
            Produto novo_Produto = this.camposParaObjetoEditar();
            this.gerente.atualizarProduto(this.cod_Escolhido, novo_Produto);
        } else {
            Produto novoProduto = this.camposParaObjeto();
            gerente.addProduto(novoProduto);
        }
        limpar_Campos();
        habilitar_Campos(false);
        this.editando = false;
        String listagem = gerente.toString();
        edtListagemProdutos.setText(listagem);
       gerente.salvarNoArquivo("ListagemProdutos.json");
        
    }//GEN-LAST:event_btnSalvarActionPerformed

   public Produto camposParaObjeto() {
        Produto produto = new Produto();
        produto.setNomeProduto(edtNome.getText());
        double value = Double.parseDouble(edtCusto.getText());
        produto.setPrecoProdutoCusto(value);
        value = Double.parseDouble(edtVenda.getText());
        produto.setPrecoProdutoVenda(value);
        int value2 = Integer.parseInt(edtEstoque.getText());
        produto.setQtdProduto(value2);
        return produto;
    }

    public Produto camposParaObjetoEditar() {
        String nomep = edtNome.getText(); //Deve ser feito utilizando segundo construtor para n찾o gerar NOVO ID
        double valueCost = Double.parseDouble(edtCusto.getText());
        double valuePrice = Double.parseDouble(edtVenda.getText());
        int valueStorage = Integer.parseInt(edtEstoque.getText());
        int idProduto = Integer.parseInt(edtID.getText());
        Produto produto = new Produto(nomep, valueStorage, valuePrice, valueCost, idProduto);
        return produto;
    }

     public void objetoParaCampos(Produto produto) {
        edtNome.setText(produto.getNomeProduto());
        edtEstoque.setText(produto.getQtdProduto() + "");
        edtCusto.setText(produto.getPrecoProdutoCusto() + "");
        edtVenda.setText(produto.getPrecoProdutoVenda() + "");
        edtID.setText(produto.getIdProduto() + "");

    }

    public void habilitar_Campos(boolean flag) {
        edtCusto.setEnabled(flag);
        edtID.setEnabled(false);
        edtNome.setEnabled(flag);
        edtVenda.setEnabled(flag);
    }

    public void limpar_Campos() {
        edtCusto.setText("");
        edtID.setText("");
        edtNome.setText("");
        edtVenda.setText("");
    }

